package locale

import (
	"fmt"
	"strings"
)

// Locale represents a single locale entry.
type Locale struct {
	Code    string
	Lang    string
	Country string
}

// Display returns a human-friendly label for the selector.
func (l Locale) Display() string {
	return fmt.Sprintf("%-36s %s", l.Lang+" — "+l.Country, "("+l.Code+")")
}

// CodeFromDisplay extracts the locale code from a Display() string.
func CodeFromDisplay(s string) string {
	start := strings.LastIndex(s, "(")
	end := strings.LastIndex(s, ")")
	if start < 0 || end <= start {
		return s
	}
	return s[start+1 : end]
}

